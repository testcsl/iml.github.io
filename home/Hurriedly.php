<p>时光荏茬，岁月青葱。转眼间，我们也从小鲜肉成为了老腊肉。我们也即将成为学长学姐，迎来一批又一批小鲜肉。让他们羡慕不用早操不用晚自习的我们。可是，我们留下更多的不正是怀恋和遗憾吗？</p>
<p>曾经豪云壮志，参加意气风发，那年，高三的我们憧憬着美丽的大学生活。传说，大学可以不用做作业，大学可以参加各种各样的大型活动，大学可以认识一群有共同志向的朋友。于是乎，应了那句话，我们用高中憧憬大学，却用大学怀恋高中。高中过得很充实，大学忙得莫名其妙：高中朋友少，却很真：大学各种聚会，茫茫人海又有几个知己。</p>
