<p>回味已经过去的大学时光，颓废，迷茫，没有找到属于自己的方向。见了大城市，什么都觉得稀奇，什么都想去做，却从来没有坚持做一件事，并把它做出成绩。每天在忙忙碌碌中度过，上课，社团，学生会，赚钱。急功近利的孩子，把人生中最美好的时光硬生生的品味出了高中的韵味。</p>
<p>和别人谈论起大学，他们的口中总是有逃课，考试不会挂之类的话。而我的大学，是似乎只剩下了忙碌。不得不说，做为一个并不聪明的我，这是一个愚蠢的选择。考试挂科，社团工作成绩平平，人际关系紧张，甚至，我没有一个知心朋友，虽然能够叫出那么多人的名字。</p>
<p>浮躁的心，浮躁的社会。刚刚回到大学，一切都是那么的新鲜。想要成为学霸，想要成为首屈一指的创业牛人，想要成为办事能人。怀着一腔热血，开始了漫长的追梦之途。每天起早贪黑，每天拖着疲惫的的身子灰土土的回到寝室，今天又碰壁了，满肚子的苦水不知往何处倾。或许是我太功利，或许是我急于求成，我得了严重的焦虑症。整天是睡不好觉，每天忧心忡忡，脾气也阴晴不定。</p>
<p>浮躁的心，浮躁的人，其实，还是要找到属于自己的舞台，并且 不断努力，才能获得成功。心太杂，心太乱，往往一事无成，最后，送大家一波鸡汤。</p>
